public class ASCII
{
public static void main(String args[])
{
       char x = 'B';
	   switch(x)
	   {
	        case A : System.out.println("UPPER");
			         break;
			case B: System.out.println("Lower");
			          break;
			case C : System.out.println("Digit");
			          break;
			default : System.out.println("special symbol");
		}
}
}
